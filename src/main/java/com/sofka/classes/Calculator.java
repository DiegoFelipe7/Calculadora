package com.sofka.classes;

public class Calculator {
    public double sum(double num1 , double num2 ){return num1+num2;}
    public double subtract(double num1 , double num2 ){return num1-num2;}
    public double multiplication(double num1 , double num2 ){return num1*num2;}
    public double split(double num1 , double num2 ){return num1/num2;}
}
